<?php

/*
 * Função somar
 * 
 * Esta função vai pegar um valo x e somar com um valor y
 * 
 * @package Controle de Estoque
 * @author douglas.poma@registrallogistica.com.br
 * 
 * @param $x float é o primeiro valor a ser somado
 * @param $y float é o segundo valor a ser somado
 * @return float
 */

function somar($x, $y) {
    return $x + $y;
}
